"""Markdown report builder for benchmark results."""

from __future__ import annotations

import logging
from datetime import datetime, timezone
from pathlib import Path
from typing import Optional

import pandas as pd

logger = logging.getLogger(__name__)


def _format_metric(value: float | None, fmt: str = ".3f") -> str:
    if value is None or (isinstance(value, float) and pd.isna(value)):
        return "N/A"
    if isinstance(value, (int, float)):
        return f"{value:{fmt}}"
    return str(value)


def build_markdown_report(
    leaderboard_df: pd.DataFrame,
    track_summaries: dict[str, pd.DataFrame],
    out_path: Path,
    tag: Optional[str] = None,
) -> Path:
    """Generate a markdown report suitable for docs/.

    Parameters
    ----------
    leaderboard_df : pd.DataFrame
        Primary leaderboard DataFrame.
    track_summaries : dict[str, pd.DataFrame]
        Mapping of track letter -> summary DataFrame.
    out_path : Path
        Output markdown file path.
    tag : str, optional
        Version or release tag to include in the report header.

    Returns
    -------
    Path
        Path to the written markdown file.
    """
    out_path = Path(out_path)
    out_path.parent.mkdir(parents=True, exist_ok=True)

    lines: list[str] = []
    lines.append("# REACH Report")
    lines.append("")
    if tag:
        lines.append(f"**Tag:** `{tag}`")
    lines.append(f"**Generated:** {datetime.now(timezone.utc).isoformat()}")
    lines.append("")

    # Leaderboard section
    lines.append("## Leaderboard")
    lines.append("")
    if leaderboard_df.empty:
        lines.append("_No leaderboard data available._")
    else:
        lines.append("| Rank | Method | Mean AP | Mean AUROC | Category | Note |")
        lines.append("|------|--------|---------|------------|----------|------|")
        for _, row in leaderboard_df.iterrows():
            rank = int(row.get("rank", 0))
            method = row.get("method_id", "")
            mean_ap = _format_metric(row.get("mean_ap"))
            mean_auroc = _format_metric(row.get("mean_auroc"))
            category = row.get("category", "")
            note = row.get("note", "")
            lines.append(f"| {rank} | {method} | {mean_ap} | {mean_auroc} | {category} | {note} |")
    lines.append("")

    # Track summaries
    lines.append("## Track Summaries")
    lines.append("")
    for track, summary_df in track_summaries.items():
        lines.append(f"### Track {track.upper()}")
        lines.append("")
        if summary_df.empty:
            lines.append("_No data for this track._")
        else:
            cols = [c for c in ["method_id", "mean_ap", "mean_auroc", "n_units"] if c in summary_df.columns]
            header = " | ".join(c.replace("_", " ").title() for c in cols)
            lines.append(f"| {header} |")
            lines.append("| " + " | ".join("---" for _ in cols) + " |")
            for _, row in summary_df.iterrows():
                vals = [_format_metric(row.get(c)) for c in cols]
                lines.append(f"| {' | '.join(vals)} |")
        lines.append("")

    # Footer
    lines.append("---")
    lines.append("*This report was auto-generated by REACH.*")
    lines.append("")

    md_content = "\n".join(lines)
    out_path.write_text(md_content, encoding="utf-8")
    logger.info(f"Markdown report written: {out_path}")
    return out_path
